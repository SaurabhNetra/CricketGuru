		  <tr><td><h1>ClT20 2nd SF: Mumbai Indians vs Somerset</h1>
          <i>
          Ronak Parpani October 8, 2011</i><br>
          <br></td></tr>
		  <tr>
		  		<td width="550">
		  			<p>
                    	<p>Mumbai Indians, face off against the English Friends Provident T20 Championship finalists Somerset. 
                        Mumbai have been severely weakened with a host of regular players, Sachin Tendulkar, Munaf Patel and 
                        Rohit Sharma amongst others missing while Somerset are without their regular skipper Marcus Trescothick. 
                        The winner of the game will play Bangalore who pipped New South Wales on their way to the final with 
                        an incredible chase.
						</p>
                        <p><img src="images/MurliKarthik.jpg" align="right" border="1" hspace="10"> 
						The Mumbai Indians have played their last 2 games on the slow pitch at the M A Chidambaram stadium and they
                         would have acclimatized themselves to the conditions while this will be Somerset's first match at the stadium.
                          Somerset have played all their games at either Hyderabad or Bangalore and will need to adapt quickly or else
                           will find themselves boarding a plane back home.
						</p>
                        <p>
						Mumbai have had to rely heavily on their foreign players especially after losing many domestic players to 
                        injury, but the form of Andrew Symonds and Kieron Pollard will be a concern for the Harbhajan Singh led side. 
                        Symonds has been a failure with the bat on numerous occasion in the tournament and Mumbai will be desperate 
                        for the Australian to get back to form and add steel to the vulnerable middle order. Pollard has failed 
                        to light up the tournament barring his half century against Cape Cobras, but he add balance to the team 
                        with his ability to be effective with the bat, ball or on the field.
						</p>
                        <p><img src="images/Alfonso.jpg" align="left" border="1" hspace="10" height="200" width="270">
                        For Somerset, skipper Alfonso Thomas and Roelof wan der Merwe have been in great form. 
                        The latter has been key to their title charge contributing heavily with both bat and ball. 
                        Murali Kartik too has been a vital cog in Somerset's engine bringing in invaluable experience
                         of playing on sub-continent tracks to help his team out.
						</p>
                        <p> 
                                Mumbai's performance so far has bordered on ridiculous for a champion side but have
                                 somehow held on to sneak through at the end. The side from India would not want 
                                 to leave it to luck to decide their fate against Somerset who have impressed 
                                 one and all with clinical performances with both bat and ball.
                                Somerset have been one of the most under-rated teams thus far, but have 
                                used that to their advantage upstaging some highly fancied teams on their 
                                way to the semi finals. 
                               </p>
                               <p> <img src="images/LasithMalinga.jpg" align="right" hspace="10" border="1" width="30%" height="20%">
                                One of the reasons for Somerset's success has been their superb batting 
                                performance at the top of the order, but their biggest test will come 
                                in the form of Lasith Malinga. Van der Merwe, Craig Kieswetter and
                                 Peter Trego have been in fantastic form and it will be upto Malinga
                                  to stop them. The Sri Lankan has the potential to derail any batting
                                   line-up and Mumbai will count on him to take quick wickets.
                                </p>
                                <p>
                                Both Somerset and the Mumbai Indians have had a frustrating past
                                 few years having come to the final stages of the tournament
                                  only to return empty handed. The Mumbai Indians failed in 
                                  the knockout stages of the Indian Premier League in the 
                                  last 2 years while Somerset too failed to grab their chances
                                   five times in the last 6 years; finishing second on all 
                                   5 occasions. 
                                Having come this far, both teams will be equally determined
                                 to not let the chance slip by again.
                                </p>
                                <p>
                                <img src="images/harbhajan.jpg" align="left" hspace="10" border="1">
                                Quotes:<br>
                                
                                Alfonso Thomas: The wicket in Chennai looks quite slow,
                                 but we have a couple of spinners and hopefully things will work out in our favour.
                                </p>
                                <p>
                                Harbhajan Singh: Result will only matter if you think
                                 too much about it. If you're looking to enjoy the game, 
                                 you give it everything. I will make sure we will give it 
                                 our best shot on the field and whatever result we get, we
                                  will be happy with it
                                </p><p>
                                Teams:<br>
                                Mumbai Indians (From): Harbhajan Singh(c), Yuzvendra Chahal, Kieron Pollard, Suryakumar Yadav, 
                                Aiden Blizzard, James Franklin, Lasith Malinga, Ambati Rayudu(w), Tirumalsetti Suman, Abu Nechim, 
                                Andrew Symonds, Rajagopal Sathish, Dilhara Fernando, Sarul Kanwar
                                </p>
                                <p>
                                Somerset (From): Alfonso Thomas(c), Alex Barrow, Jos Buttler, Nick Compton, Adam Dibble, 

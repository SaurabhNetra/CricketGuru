		  <tr><td><h1>Pietersen targets next World Cup</h1>
          <i>
          Ronak Parpani October 9, 2011</i><br>
          <br></td></tr>
          <tr>
		  		<td width="500">
		  			<p>
                    	<p>
                    <p><img src="images/kevinp.jpg" align="left" hspace="10" vspace="5" height="30%" width="40%" border="1">
                    	Kevin Pietersen has tried to put to bed talk about his future in one-day cricket by insisting that
                         he wants to play for England until at least the 2015 World Cup in Australia and New Zealand. 
                         Pietersen is back in the ODI set-up for the series in India, having been rested for the last
                          month of the home season, but is under pressure to perform after a lean two years in the 50-over format.
                           </p><p>                          
                            
                            Pietersen is likely to slot back in at No. 4 when the series starts next week
                             following two warm-up matches although in his absence the likes of Ravi Bopara 
                             and Jonny Bairstow have staked their claims. Before leaving for the tour 
                             Alastair Cook, the one-day captain, spoke about wanting Pietersen involved 
                             all the way to the next World Cup but also said that players had to earn their
                              selection with consistent performances. Pietersen hasn't scored an ODI hundred 
                              since November 2008 and over the last two years his average has languished in 
                              the low 20s.
                             </p><p> 
                             <img src="images/kpworldcup.jpg" align="right" hspace="10" vspace="5" height="25%" width="45%" border="1">
                            <q>I don't know how many times I need to say it till you stop asking the questions
                            ,</q> Pietersen said. <q>I want to continue playing until the 2015 World Cup, and then 
                            we'll reassess. No one's ever guaranteed their place - for their country, for their
                             county, for their club side.</q>
                            </p><p>  
                            <q>There's a lot of different permutations and different things that can happen. 
                            It's just a case of enjoying every day, and also realising how fortunate we are. 
                            If there's any place you want to come and play one-day international cricket, 
                            it's here in India.</q>
                             </p><p> 
                            Pietersen used his break from the game - which included missing the one-dayers 
                            against India and the Twenty20s against West Indies - to completely get away 
                            from cricket. He moved house in London and spent time with his family, which has 
                            become an increasingly important part of his life and the main reason he has 
                            admitted to not being a fan of long tours.
                             </p><p> 
                            He has also said that, while retaining the hunger to play all three forms of
                             international cricket, he enjoyed not having a gruelling schedule of training.
                              
                              <q>I wanted to take a complete break, and get that bug back for training. That's the first break
                               I've had without an injury for England. It was much appreciated.</q>
                             </p><p> 
                             <img src="images/fit.jpg" border="1" align="left" hspace="10" vspace="10"  height="20%" width="45%"> 
                            <q>The hardest part of playing all three forms of the game is turning up on
                             training day and doing warm-ups - I think it takes a couple of years off your career. 
                             To not have to turn up to training days was an absolute blessing. But you do miss the 
                             cricket, participating and competing - because we're all competitors.</q>
                             </p><p> 
                            After he was left out of the one-day squad last month, it emerged that Pietersen had
                             a wrist injury that required some management and he had an injection before leaving
                              for India. <q>I've got something in my wrist,<q> he said. <q>I had a cortisone injection
                               a few days ago, and cortisone just before I left. It's just a case of getting through
                                this tour and seeing where we are at the end.</q>
                             </p><p> 
                            Pietersen, though, will need to fight through some minor discomfort because England won't 
                            wait for him to rediscover his one-day mojo forever.
                       </p>         
                    </p>	
                    </p>	

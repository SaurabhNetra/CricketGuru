		  <tr><td><h1>Shoaib sells the drama</h1>
          <i>
          Ronak Parpani October 9, 2011</i><br>
          <br></td></tr>
          <tr>
		  		<td width="550">
		  			<p>
                    		<p> 
                            <img src="images/booklaunch.jpg" align="left" height="30%" width="35%" border="1" hspace="10" vspace="10">
                            	The first thing you realise when you read Shoaib Akhtar's autobiography is that much of the media
                                 reaction to it is a distraction from the book's true merits. Yes, he has admitted to ball-tampering
                                 , delivered questionable opinions on Sachin Tendulkar and Rahul Dravid, and blasted some of the high
                                  priests of Pakistan cricket. But he has also said a great deal that is more interesting, more 
                                  important, and evidently more heartfelt.
								</p><p>
										As a device to enhance your pleasure of the game, this book delivers. Perhaps the most
                                         absorbing, at times even riveting, parts of it are the first four chapters, which 
                                         detail Shoaib's ordeals before he became a celebrity. He was born into modest 
                                         circumstances, a sickly child who at one point nearly died of whooping cough. The 
                                         family struggled with money and would sometimes go hungry.
								</p><p>
                                    Despite the financial constraints, his parents worked hard to 
                                    instill upright values in the children and ensure them an education. 
                                    Shoaib tells us he was an ace student, and also a natural prankster. 
                                    As a result, he was frequently in trouble. The pattern of conflicts 
                                    that marked his international career was set early on.
                                   </p><p>
                                   <img src="images/wasimwaqar.jpg" border="1" align="right" hspace="10"
                                   vspace="10" height="250" width="180"> 
                                    Cricket did not become a focus until his teenage years, when Shoaib's 
                                    passion for bowling fast was unleashed. Before that, he played informal 
                                    street games, including gilli-danda, and ran a lot - everywhere, 
                                    aimlessly - because it made him feel free. Once he discovered cricket, 
                                    he was drawn to role models, finding instant inspiration in 
                                    Imran Khan's dynamic and towering figure. At the Pindi Club he 
                                    saw his idols Imran, Wasim Akram and Waqar Younis practise under lights.
                                     That was when he made a personal vow to don the Pakistan colours.
                                    </p><p>
                                    All through the book Shoaib confronts his bad-boy image head-on, 
                                    but his reflections are unlikely to sway anyone. He admits having 
                                    little patience for rules and regulations, resents being preached 
                                    to, and hates restrictions of any kind. He complains about never 
                                    having had a proper mentor, but more likely it would not have been 
                                    possible for even the most well-meaning mentor to get through to him.
                                    </p><p>
                               <img src="images/shoaibakhtar21tn.jpg" height="20%"  
                               width="40%" align="left" hspace="10" vspace="5">
                                    His self-indulgent nature comes across loudly, as we are
                                     told of his love for money, girls, and even his own company.
                                      Speed, above everything, is his true love, and garners a long
                                       chapter by itself. All this underscores his ability to polarise.
                                        Shoaib's supporters will find him refreshingly honest; his 
                                        detractors will deride him for confirming their worst assumptions.
                                    </p><p>
                                    When we come to the chapters covering his international 
                                    career, it is disappointing to see a lack of serious introspection. 
                                    All his great spells are glossed over, leaving the connoisseur unfulfilled.
                                     You keep expecting to be placed inside the fast bowler's head as he 
                                     psycho-analyses the batsman, adjusts the field, and contemplates
                                      his wicket-taking plans, but it never happens. Shoaib could have 
                                      entertained his readers with a ringside view of these intricacies; 
                                      it feels like a golden opportunity lost. Also slightly disappointing 
                                      are the book's occasional typos and misspellings. The persistent 
                                      insertion of a hyphen between "T" and "20" is especially grating.
                                    </p><p>
                                    Nevertheless the overall package is a highly enjoyable one. 
                                    Co-author Anshu Dogra has polished the material into a coherent
                                     and flowing narrative, yet still allowed Shoaib's first-person 
                                     voice to be heard clearly. Urdu and Punjabi phrases are interspersed
                                      here and there, conveying the thought precisely and to the 
                                      understanding reader's great amusement.
                                    </p><p>
                                    Anecdotes, often the choicest part of a memoir, are peppered 
                                    throughout. There are accounts of Shoaib in college as he drives 
                                    a motorbike through the principal's office, gets suspended for 
                                    playing cricket in front of the girls' building, and convinces 
                                    a channa wala to serve him free meals because one day Shoaib will 
                                    be a famous cricketer.
                                    </p><p>
                                    Shoaib tells us about the anxiety of appearing for domestic
                                     cricket trials in Lahore, and the joy of catching the eye of 
                                     Zaheer Abbas. We learn how, just before breaking into Test 
                                     cricket, he spent an emotionally wrenching period in Karachi
                                      rooming with his buddy Saqlain Mushtaq when they were 
                                      struggling cricketers and the city was in turmoil.
                                    </p><p>
                                    The book's tone is sometimes conversational, sometimes argumentative,
                                     with seamless transitions into languid storytelling one minute, 
                                     breathless rhetoric the next. In this, the narrator sounds every bit
                                      the Shoaib Akhtar we know from his public persona.
                                    </p><p>
                                    Every now and then there is also some touching human moment - 
                                    getting tongue-tied when an attractive Irish girl starts a 
                                    conversation in a bar, buying his first car, looking up an 
                                    old benefactor after becoming a star, revisiting old haunts 
                                    in his hometown of Rawalpindi.
                                    </p><p>
                                    Naturally there is a good deal of score-settling as well, 
                                    some of which - including targeted jabs at the likes of Wasim Akram, 
                                    Javed Miandad, and Tendulkar - has been the subject of recent news cycles.
                                     Among all these, I found the description of Shoaib's administrative duel
                                      with former PCB chairman Nasim Ashraf particularly valuable. 
                                  <img src="images/CONTROVERSIALLY.jpg" align="right" height="17%" width="30%"  vspace="10" hspace="10">
                                      The drama 
                                      is vividly sketched over several pages as Shoaib struggles and eventually 
                                      succeeds in getting his PCB-enforced ban reversed by pulling political strings.
                                       His account provides sharp insight into Pakistan cricket's backroom ploys and
                                        validates a great deal of drawing-room chatter.
                                    </p><p>
                                    All said and done, you have to commend the man for a job well done. The very appearance
                                     of his book is a feat in itself: written output from Pakistan's cricketers has been
                                      sparse. Shoaib may have carried an image of carefree indiscipline for most of his
                                     career, but he has certainly demonstrated he has the discipline to produce a book 
                                    with impact. In this he has outdone several other famous cricketing names from Pakistan.

                            </p>
                    </p>	

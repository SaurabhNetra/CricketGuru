		  <tr><td><h1>RCB rip through second consecutive 200-plus target</h1>
          <i>
          Sidharth Monga October 7, 2011</i><br>
          <br><h4>Royal Challengers Bangalore 204 for 4 
          (Gayle 92, Kolhi 84*, Cummins 4-45) beat New South Wales 203 for 2 (Warner 123*, D Smith 62) by six wickets</h4></td></tr>
          <tr>
		  		<td width="550">
		  			<p>
                                        <p>
                                        Bring the ramparts from Galle, the wall from China, the many castles from Europe. Bring all the defence buildings, tanks, experts. Place them in the Chinnaswamy Stadium, fast becoming the Wanderers of the northern hemisphere. Hide your 200-plus targets behind them. Chris Gayle, Tillakaratne Dilshan and Virat Kolhi will still chase them down at this venue. David Warner tonight became the first man to score back-to-back Twenty20 centuries, but even more incredibly the Royal Challengers Bangalore became the first team to chase down 200-plus scores in consecutive matches, that too with nine balls to spare.</p>
                                       
                                        <p>
                <img src="images/davidwarner.jpg" align="right" border="1" hspace="10">Before the match, Gayle and Warner, soon to be opening partners for Sydney Thunder in the Big Bash League, were seen chatting with each other. When walking back, Warner took a look at Gayle's bat. He held it sideways, and his jaw dropped looking at the thickness of the leading edge. They both had a laugh, and went their separate ways for the night. Gayle laughed the final laugh, though, as his 92 off 41 in company with Kohli's unbeaten 84 off 49 outdid Warner's extraordinary 123 off 68 and Daniel Smith's 62 off 42. That leading edge was never hit on a night when clean hits tested the size of the stadium, and the half-hits managed to clear the short boundaries. Warner and Gayle dented the roof once each, but Gayle - symbolically - managed to send one of them out of the stadium. Records fell like crops during a hurricane, and the crowd - who must tire of this some day - matched the hitting with their incredible noise, appreciating both teams, only the home side more loudly.</p>
                <p>
                The Royal Challengers Bangalore chose to chase again, packing their side with an extra batsman. Clearly all their eggs were in the basket woven together by Dilshan, Gayle and Kolhi. The basket proved to be strong enough. Incredible as it may sound, it was perhaps Dilshan who made the most crucial contribution even though he managed just four off nine. On a night that the other 34.3 overs went for 407 runs, Dilshan bowled his four overs for 10 runs at the top of the NSW innings. On a night that 26 sixes and 34 fours were hit, Dilshan went for none, as the Royal Challengers registered their least expensive Powerplay overs of this tournament, going for 47 runs. That Dilshan registered the Royal Challengers' most economic four-over figures was perhaps the most crucial record on a night that must have sent statisticians into frenzy.
                </p>
                <p><img src="images/gayle.jpg" align="left" border="1" hspace="10" height="165" width="225">
                Daniel Vettori's captaincy has come in for a bit of criticism in this event, mainly for under-using his part-time spinners, but he got it right when he asked Dilshan to bowl the first over of the night. It is hard to say if it was the batsmen were a touch too circumspect. The question must only arise because the contrast between his figures and the rest was stark. He did seem to do the right things, though: he didn't give the batsmen the pace or the rank bad deliveries. That he managed to frustrated Shane Watson into the rare injudicious shot of the night, costing him his wicket drew a bit of caution, and he rushed through his quota.
                </p>
                <p>
                Apart from that, the rest of the NSW innings - the whole match in fact - was carnage, par for the Chinnaswamy course. Daniel Smith cheeked his way to a maiden fifty, even managing to out-hit Warner. He late-cut delicately, drove over cover with aplomb, slogged savagely, and enjoyed a few edges, even as Warner sized up the bowlers and the field, a time bomb ticking over.
                </p>
                <p>
                Once he got into acceleration mode in the 10th over, hitting a full toss from S Aravind for four past long-on, there was no stopping him. He scored 81 off the last 33 balls he faced as NSW looted 131 from the last 10.1 overs. Of the 11 sixes that Warner hit, two stood out: the regulation slog over midwicket that hit the roof, the switch-slog off the medium-pace of Raju Bhatkal, which cleared wide long-off with ease. Dirk Nannes, who many believe shouldn't have been allowed to play this tournament, went for 51 in his four, reaching a tournament economy-rate of 10.42 for a solitary wicket.
                </p>
                <p>
                
                Even after 23 runs off the last over, once again damaging poor Aravind's figures, there was uncertainty around the total. NSW would need to cut singles, keep twos down to one, and take miraculous catches. They dropped three of them. Dilshan's miss didn't hurt them much, but Kohli made them pay for two lives. One of them was a sitter dropped by Pat Cummins when Kohli was on 44. Ironically, after Cummins had taken three wickets to give NSW another life, the miraculous catch offered itself. Kohli had been under pressure then, the last 14 balls had brought nine runs, and they now needed 24 off 16. A highly unreliable lower order waited. Kohli slogged, the mis-hit went to the right and in front of long-on, Mosies Henriques charged after it, but couldn't convert the half chance.
                </p>
                <p>
                Kolhi proceeded to knock off the rest of the target with ease. Just as easy as it had earlier seemed for Kohli and Gayle. It seemed all they needed to do was raise the bat high; by some sort of design, they would hit the ball well and in the gaps. At times Gayle wouldn't even need the back lift. He just caressed a few full deliveries for parallel sixes and fours. It was a highlights package that had to be further edited for time. A good old friend, the yorker, was sorely missed among the crowd of fancy slower balls of all varieties. The only one produced got NSW a wicket.</p>
                    </p>	
